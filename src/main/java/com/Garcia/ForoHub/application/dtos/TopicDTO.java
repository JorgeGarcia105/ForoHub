package com.Garcia.ForoHub.application.dtos;

import java.util.Date;

public class TopicDTO {
    private Long id;
    private String titulo;
    private String mensaje;
    private Date fechaCreacion;
    private String status;
    private String autor;
    private Long cursoId;  // ID de Curso
    private Long respuestaId;  // ID de Response

    public TopicDTO(Long id, String titulo, String autor, Date fechaCreacion) {
    }

    public TopicDTO(Long id, String titulo, String mensaje, Date fechaCreacion, String status, String autor, Long cursoId, Long respuestaId) {
        this.id = id;
        this.titulo = titulo;
        this.mensaje = mensaje;
        this.fechaCreacion = fechaCreacion;
        this.status = status;
        this.autor = autor;
        this.cursoId = cursoId;
        this.respuestaId = respuestaId;
    }

    // Getters y setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getMensaje() {
        return mensaje;
    }

    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }

    public Date getFechaCreacion() {
        return fechaCreacion;
    }

    public void setFechaCreacion(Date fechaCreacion) {
        this.fechaCreacion = fechaCreacion;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public Long getCursoId() {
        return cursoId;
    }

    public void setCursoId(Long cursoId) {
        this.cursoId = cursoId;
    }

    public Long getRespuestaId() {
        return respuestaId;
    }

    public void setRespuestaId(Long respuestaId) {
        this.respuestaId = respuestaId;
    }
}
